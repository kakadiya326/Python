##########################################################
############-----------Sorting------------################
##########################################################

s=[4,6,9,8,2,1]
s.sort() #sort() will modify in source list and give [1,2,4,6,8,9]
print('sort : ',s)

sr=[4,6,9,8,2,1]
sr.sort(reverse=True) #sort() will modify in source list and give [9,8,6,4,2,1]
print('sort reverse : ',sr)

srd=[4,6,9,8,2,1]
print('sorted : ',sorted(srd)) #sorted() will not modify in source list and give [1,2,4,6,8,9]
print('sorted reverse : ',sorted(srd,reverse=True)) #source list will remain same [4,6,9,8,2,1]

